        "            f\"idx={idx} is out of bounds for X with {len(X)} rows.\"\n",
        "        )\n",
        "\n",
        "    explainer, shap_values = _get_shap_values(model, X)\n",
        "\n",
        "    shap_explanation = shap.Explanation(\n",
        "        values=shap_values[idx],\n",
        "        base_values=explainer.expected_value,\n",
