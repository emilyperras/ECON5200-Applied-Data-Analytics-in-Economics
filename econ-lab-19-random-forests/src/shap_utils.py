        "# ---------------------------------------------------------------------------\n",
        "# 1. explain_prediction\n",
        "# ---------------------------------------------------------------------------\n",
        "def explain_prediction(\n",
        "    model: TreeModel,\n",
        "    X: pd.DataFrame,\n",
        "    idx: int,\n",
        "    title_suffix: str = \"\",\n",
        "    save_path: str | None = None,\n",
        ") -> None:\n",
        "    \"\"\"\n",
        "    Generate a SHAP waterfall plot explaining a single prediction.\n",
        "\n",
        "    A waterfall plot starts from the model's expected output (E[f(x)]),\n",
