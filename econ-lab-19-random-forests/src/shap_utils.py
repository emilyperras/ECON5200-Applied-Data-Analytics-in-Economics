        "    receives its \"fair share\" of the prediction gap from the baseline.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    model       : fitted sklearn tree ensemble (RandomForest or GBR)\n",
        "    X           : feature matrix as a DataFrame (test or full set)\n",
